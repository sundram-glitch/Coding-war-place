//CGC
class CGC_AB 
{
	public static void main(String[] args){
		Byte B = Byte.valueOf((byte)40);
		Short S = Short.valueOf((short)50);
		Integer I = Integer.valueOf(/*(int)*/60);
		Long L = Long.valueOf(/*(long)*/70L);
		Float F = Float.valueOf(/*(float)*/80F);;
		Double D = Double.valueOf(/*(double)*/90D);
		Character CH = Character.valueOf(/*(char)*/'a');
		Boolean BO = Boolean.valueOf(/*(boolean)*/true);
		System.out.println("B:"+B);
		System.out.println("S:"+S);
		System.out.println("I:"+I);
		System.out.println("L:"+L);
		System.out.println("F:"+F);
		System.out.println("D:"+D);
		System.out.println("CH:"+CH);
		System.out.println("BO:"+BO);
		System.out.println("end of main!");
	}
}
